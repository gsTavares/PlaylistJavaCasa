public class AulaTiposPrimitivos {
    
    public static void main(String[] args) {
        // Tipos primitivos
        // int, double, float, char, byte, short, long, boolean
        
        // 4 bytes em memória
        int idade = 10;
        
        // 8 bytes em memória
        long numeroGrande = 1000000;
        
        // 8 bytes em memória
        double salarioDouble = 2000.0;
        
        // 4 bytes em memória
        float salarioFloat = 2500;
        
        // 1 byte em memória
        byte idadeByte = 10;
        
        // 2 bytes em memória
        short idadeShort = 10;
        
        // 1 bit em memória
        boolean verdadeiro = true;
        boolean falso = false;
        
        // 2 bytes em meória
        char caractere = 'M';
        
        System.out.println("A idade é "+idade);
        
    }
    
}
